package com.longtime.ajy.mweb.model;







public class DomainTemplate  {

	private String id; //主键
	
	private String tid;//模版ID
	
	private String sid;//风格ID
	
	private String domain; //域
	
	private String mid; //模块ID，冗余字段，方便查询
	
	private String isdel="0";//0可用 1已删除
	
	
	public static int TYPE_MODULE = 0;//模版
	
	public static int TYPE_STYLE = 1;//风格

	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}

	public String getMid() {
		return mid;
	}

	public void setMid(String mid) {
		this.mid = mid;
	}

	public String getTid() {
		return tid;
	}

	public void setTid(String tid) {
		this.tid = tid;
	}

	public String getSid() {
		return sid;
	}

	public void setSid(String sid) {
		this.sid = sid;
	}

	public String getIsdel() {
		return isdel;
	}

	public void setIsdel(String isdel) {
		this.isdel = isdel;
	}
	
}
