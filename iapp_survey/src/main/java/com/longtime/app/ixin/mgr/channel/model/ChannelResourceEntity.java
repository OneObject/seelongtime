package com.longtime.app.ixin.mgr.channel.model;
/**
 * 栏目和资源的关联表
 * @author fangxinyuan
 *
 */
public class ChannelResourceEntity {
	
      private String id;
      
      private String channelid;//栏目id
      
      private String resourceid;//关联的资源id
      
      private int  restype;//资源类型
      
      private String rescover;//资源封面
      
      private String resname;//资源名称
      
      private long weight;//权重

	  private Long timestamp;
      
      
      public static int ALL_RESTYPE=-1;//查询所有的分类
      
      public static int SUCAI_RESTYPE=0;//素材
      
      public static int SURVEY_RESTYPE=1;//调研
      
      public static int VOTE_RESTYPE=2;//投票
      
      public static int REWARD_RESTYPE=3;//抽奖
      
      public static int RANK_RESTYPE=4;//排行榜
      
      public static int COURSE_RESTYPE=5;//课程
      
      public static int EXAM_RESTYPE=6;//考试
      
      public static int ACTIVITY_RESTYPE=7;//活动
      
      public static int TASK_RESTYPE=8;//作业
      
      
      
      

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getChannelid() {
		return channelid;
	}

	public void setChannelid(String channelid) {
		this.channelid = channelid;
	}

	public String getResourceid() {
		return resourceid;
	}

	public void setResourceid(String resourceid) {
		this.resourceid = resourceid;
	}

	public int getRestype() {
		return restype;
	}

	public void setRestype(int restype) {
		this.restype = restype;
	}


	public String getRescover() {
		return rescover;
	}

	public void setRescover(String rescover) {
		this.rescover = rescover;
	}

	public String getResname() {
		return resname;
	}

	public void setResname(String resname) {
		this.resname = resname;
	}

	public long getWeight() {
		return weight;
	}

	public void setWeight(long weight) {
		this.weight = weight;
	}

	public Long getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(Long timestamp) {
		this.timestamp = timestamp;
	}
}
