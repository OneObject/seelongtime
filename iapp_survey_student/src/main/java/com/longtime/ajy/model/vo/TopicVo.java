public class TopicVo {
	
	private Topic topic;

	private List<PraiseHistory> praiseHistorylist;
    
    private List<Comment> commentlist;


	public Topic getTopic() {
		return topic;
	}

	public void setTopic(Topic topic) {
		this.topic = topic;
	}

	public List<PraiseHistory> getPraiseHistorylist() {
		return praiseHistorylist;
	}

	public void setPraiseHistorylist(List<PraiseHistory> praiseHistorylist) {
		this.praiseHistorylist = praiseHistorylist;
	}

	public List<Comment> getCommentlist() {
		return commentlist;
	}

	public void setCommentlist(List<Comment> commentlist) {
		this.commentlist = commentlist;
	}

    
    
    
    
}
