package com.longtime.app.ixin.mgr.model;


public class AutoResponseRule {
    private String id;
    private String domain;
    private String name;
    private String keywords;
    private int responseAll = AutoResponseRule.RESPONSE_ALL_NO;
    private String matterIds;
    private int isdel = IS_DEL_NO;
    private String creater;
    private Long createTime;
    private String updater;
    private Long updateTime;
    private String type = TYPE_NEWS;
    private String content;
    private int matchAll = AutoResponseRule.MATCH_ALL_NO;
    
    public static String TYPE_TEXT = "1";
    public static String TYPE_NEWS = "0";
    
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getDomain() {
        return domain;
    }
    public String getMatterIds() {
        return matterIds;
    }
    public void setMatterIds(String matterIds) {
        this.matterIds = matterIds;
    }
    public void setDomain(String domain) {
        this.domain = domain;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getKeywords() {
        return keywords;
    }
    public void setKeywords(String keywords) {
        this.keywords = keywords;
    }
    public int getIsdel() {
        return isdel;
    }
    public void setIsdel(int isdel) {
        this.isdel = isdel;
    }

    public String getCreater() {
        return creater;
    }
    public void setCreater(String creater) {
        this.creater = creater;
    }
    public String getUpdater() {
        return updater;
    }
    public void setUpdater(String updater) {
        this.updater = updater;
    }
    public Long getCreateTime() {
        return createTime;
    }
    public void setCreateTime(Long createTime) {
        this.createTime = createTime;
    }
    public Long getUpdateTime() {
        return updateTime;
    }
    public void setUpdateTime(Long updateTime) {
        this.updateTime = updateTime;
    }
    public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getMatchAll() {
        return matchAll;
    }
    public void setMatchAll(int matchAll) {
        this.matchAll = matchAll;
    }

    /**
     * 未删除
     */
    public static int IS_DEL_NO = 0;
    
    /**
     * 已删除
     */
    public static int IS_DEL_YES = 1;
    /**
     * 回复全部：否
     */
    public static int RESPONSE_ALL_NO = 0;
    /**
     * 回复全部：是
     */
    public static int RESPONSE_ALL_YES = 1;
    /**
     * 完全匹配：是
     */
    public static int MATCH_ALL_NO = 0;
    /**
     * 完全匹配：是
     */
    public static int MATCH_ALL_YES = 1;

    public int getResponseAll() {
        return responseAll;
    }
    public void setResponseAll(int responseAll) {
        this.responseAll = responseAll;
    }

}
