package com.longtime.app.news.vo;

public class DetailMsg {

	private String title;
	private String datelineFormat;
	private String imgUrl;
	private String content;
	private String id;
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDatelineFormat() {
		return datelineFormat;
	}
	public void setDatelineFormat(String datelineFormat) {
		this.datelineFormat = datelineFormat;
	}
	public String getImgUrl() {
		return imgUrl;
	}
	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
}
