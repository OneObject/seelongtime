package com.longtime.ajy.model;


public class ResourceEntity {

    private String id;

    private String domain;
    
    private String title;
    
    private String cover;
    
    private String remark;
    
    private int openstatus;
    
    private int releasestatus;
    
    private int isdel;
    
    private Long createtime;
    
    private Long modifytime;
    
    private Long releasetime;
    
    private String resid;
    
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	

	public int getIsdel() {
		return isdel;
	}
	
	
	public String getResid() {
		return resid;
	}

	public void setResid(String resid) {
		this.resid = resid;
	}

	public void setIsdel(int isdel) {
		this.isdel = isdel;
	}
	
	public Long getModifytime() {
		return modifytime;
	}

	public void setModifytime(Long modifytime) {
		this.modifytime = modifytime;
	}

	public Long getReleasetime() {
		return releasetime;
	}

	public void setReleasetime(Long releasetime) {
		this.releasetime = releasetime;
	}

	public Long getCreatetime() {
		return createtime;
	}

	public void setCreatetime(Long createtime) {
		this.createtime = createtime;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}


	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getCover() {
		return cover;
	}

	public void setCover(String cover) {
		this.cover = cover;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public int getOpenstatus() {
		return openstatus;
	}

	public void setOpenstatus(int openstatus) {
		this.openstatus = openstatus;
	}

	public int getReleasestatus() {
		return releasestatus;
	}

	public void setReleasestatus(int releasestatus) {
		this.releasestatus = releasestatus;
	}

	@Override
	public String toString() {
		return String.format("ResourceEntity [id=%s, domain=%s, title=%s, cover=%s, remark=%s, openstatus=%s, releasestatus=%s, isdel=%s, createtime=%s, modifytime=%s, releasetime=%s, resid=%s]",
			id,
			domain,
			title,
			cover,
			remark,
			openstatus,
			releasestatus,
			isdel,
			createtime,
			modifytime,
			releasetime,
			resid);
	}

    

}
