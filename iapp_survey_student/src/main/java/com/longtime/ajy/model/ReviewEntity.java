package com.longtime.ajy.model;

import com.longtime.common.model.Entity;

/**
 * @since 2013-10-10
 * @author yangwk
 * 
 */
public class ReviewEntity implements Entity<String> {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private String id;

	private String sid; //资源id
	
	private String uid; //用户id
	
	private String type; //用户态度
	
	private Long dateCreated;

	private String userCreated;

	private Long dateLastModified;

	private String userLastModified;
	
	public static String AGREE = "1"; //赞同
	
	public static String CANCEL_AGREE = "2"; //取消赞同
	
	public static String DISAGREE = "3"; //反对
	
	public static String CANCEL_DISAGREE = "4";//取消反对

	

	public String getSid() {
		return sid;
	}

	public void setSid(String sid) {
		this.sid = sid;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Long getDateCreated() {
		return dateCreated;
	}

	public void setDateCreated(Long dateCreated) {
		this.dateCreated = dateCreated;
	}

	public String getUserCreated() {
		return userCreated;
	}

	public void setUserCreated(String userCreated) {
		this.userCreated = userCreated;
	}

	public Long getDateLastModified() {
		return dateLastModified;
	}

	public void setDateLastModified(Long dateLastModified) {
		this.dateLastModified = dateLastModified;
	}

	public String getUserLastModified() {
		return userLastModified;
	}

	public void setUserLastModified(String userLastModified) {
		this.userLastModified = userLastModified;
	}
	
	
}
