package com.longtime.app.base.controller.vo;

public class PermissionShort {
	
	private String id;
	
//	private String name;
	
	private int depth;

	private String url;
	
	private String sourceId;
	
    public PermissionShort() {
        super();
    }
	public PermissionShort(String id, int depth, String url) {
        super();
        this.id = id;
        this.depth = depth;
        this.url = url;
    }
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
    public int getDepth() {
        return depth;
    }
    public void setDepth(int depth) {
        this.depth = depth;
    }
    public String getUrl() {
        return url;
    }
    public void setUrl(String url) {
        this.url = url;
    }
    public String getSourceId() {
        return sourceId;
    }
    public void setSourceId(String sourceId) {
        this.sourceId = sourceId;
    }
}
