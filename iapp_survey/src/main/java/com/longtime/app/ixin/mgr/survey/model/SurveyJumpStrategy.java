package com.longtime.app.ixin.mgr.survey.model;

public class SurveyJumpStrategy {

	private String id; //主键
	
	private String survey_id; //调研id
	
	private String source_qid; //设置逻辑跳转的题目ID
	
	private String option_id; //选项id，选择该选项则进行跳转，any表示任意选项
	
	private String jump_to; //跳转到的试题,over表示结束调研
	
	private String domain; //域
	
	private Long create_time; //创建时间
	
	private String creator; //uuid 创建人ID
	
	private Long update_time; //最后修改时间
	
  	private String updater; //uuid 最后修改人ID
	
	private int isdel; //是否删除 0未删除 1已删除

	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSurvey_id() {
		return survey_id;
	}

	public void setSurvey_id(String survey_id) {
		this.survey_id = survey_id;
	}

	public String getSource_qid() {
		return source_qid;
	}

	public void setSource_qid(String source_qid) {
		this.source_qid = source_qid;
	}

	public String getOption_id() {
		return option_id;
	}

	public void setOption_id(String option_id) {
		this.option_id = option_id;
	}

	public String getJump_to() {
		return jump_to;
	}

	public void setJump_to(String jump_to) {
		this.jump_to = jump_to;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}

	public Long getCreate_time() {
		return create_time;
	}

	public void setCreate_time(Long create_time) {
		this.create_time = create_time;
	}

	public String getCreator() {
		return creator;
	}

	public void setCreator(String creator) {
		this.creator = creator;
	}

	public Long getUpdate_time() {
		return update_time;
	}

	public void setUpdate_time(Long update_time) {
		this.update_time = update_time;
	}

	public String getUpdater() {
		return updater;
	}

	public void setUpdater(String updater) {
		this.updater = updater;
	}

	public int getIsdel() {
		return isdel;
	}

	public void setIsdel(int isdel) {
		this.isdel = isdel;
	}

	@Override
	public String toString() {
		return String
				.format("SurveyJumpStrategy [id=%s, survey_id=%s, source_qid=%s, option_id=%s, jump_to=%s, domain=%s, create_time=%s, creator=%s, update_time=%s, updater=%s, isdel=%s]",
						id, survey_id, source_qid, option_id, jump_to, domain,
						create_time, creator, update_time, updater, isdel);
	}

	
	
}
