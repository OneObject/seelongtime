package com.longtime.ajy.model;

import org.jongo.marshall.jackson.oid.Id;



/**
 * @since 2013-9-2
 * @author yangwk
 * 
 */
public class TesteePaperAnswerModel{

	@Id
	private String _id;
	
	private String testee_paper_id;//答卷id
	
	private String question_id; //试题id
	
	private String answer; //答案
	
	private String score; //用户的得分


	private Float qscore=0f; //题目设置的分数

	private int num; //试卷份数
    
    private String domain;
	

	public String get_id() {
		return _id;
	}

	public void set_id(String _id) {
		this._id = _id;
	}

	public String getTestee_paper_id() {
		return testee_paper_id;
	}

	public void setTestee_paper_id(String testee_paper_id) {
		this.testee_paper_id = testee_paper_id;
	}

	public String getQuestion_id() {
		return question_id;
	}

	public void setQuestion_id(String question_id) {
		this.question_id = question_id;
	}

	public String getAnswer() {
		return answer;
	}

	public void setAnswer(String answer) {
		this.answer = answer;
	}

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

    public String getDomain() {
        return domain;
    }

    public void setDomain(String domain) {
        this.domain = domain;
    }

	public Float getQscore() {
		return qscore;
	}

	public void setQscore(Float qscore) {
		this.qscore = qscore;
	}
}
