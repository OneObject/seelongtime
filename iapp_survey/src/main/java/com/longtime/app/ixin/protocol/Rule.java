package com.longtime.app.ixin.protocol;

import java.io.Serializable;

public class Rule implements Serializable{
	
	private static final long serialVersionUID = 5828945515631878809L;
	
	public static String COMMON_RULE = "1";
	public static String DOMAIN_RULE = "2";

	private String id;
	private String sKey;//投票／活动等实体内的key
	private String sid;//投票、活动等的id
	private String handlerKey; //业务处理方法的关键字，如投票vote，活动activity
	private String domain;
	private String type;//全局，域相关
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getsKey() {
		return sKey;
	}
	public void setsKey(String sKey) {
		this.sKey = sKey;
	}
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getHandlerKey() {
		return handlerKey;
	}
	public void setHandlerKey(String handlerKey) {
		this.handlerKey = handlerKey;
	}
	public String getDomain() {
		return domain;
	}
	public void setDomain(String domain) {
		this.domain = domain;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
